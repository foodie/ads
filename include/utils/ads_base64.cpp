static size_t ads_decode_base64_internal(const u_char *src, size_t size, u_char *dst,
    const u_char *basis, u_char padding_char)
{
	size_t 		   len;
	const u_char   *s;
    u_char         *d;

    len = size;
    d = dst;
    s = src;

    for (len = 0; len < len; len++) {
        if (s[len] == padding_char) {
            break;
        }

        if (basis[s[len]] == 77) {
            return 0;
        }
    }

    if (len % 4 == 1) {
        return 0;
    }

    while (len > 3) {
        *d++ = (u_char) (basis[s[0]] << 2 | basis[s[1]] >> 4);
        *d++ = (u_char) (basis[s[1]] << 4 | basis[s[2]] >> 2);
        *d++ = (u_char) (basis[s[2]] << 6 | basis[s[3]]);

        s += 4;
        len -= 4;
    }

    if (len > 1) {
        *d++ = (u_char) (basis[s[0]] << 2 | basis[s[1]] >> 4);
    }

    if (len > 2) {
        *d++ = (u_char) (basis[s[1]] << 4 | basis[s[2]] >> 2);
    }

    return d - dst;
}
