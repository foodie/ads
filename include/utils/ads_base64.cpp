size_t ads_encode_base64(const char *src, size_t size, char *dst)
{
    static u_char   basis64[] =
            "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789+/";
    return ads_encode_base64_internal((const u_char*)src, size, (u_char*)dst, basis64, 1, '=');
}


size_t ads_encode_base64url(const char *src, size_t size, char *dst)
{
    static u_char   basis64[] =
            "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789-_";
    return ads_encode_base64_internal((const u_char*)src, size, (u_char*)dst, basis64, 0);
}


static size_t ads_encode_base64_internal(const u_char *src, size_t size, u_char *dst, 
	const u_char *basis, int padding, u_char padding_char)
{
    const u_char   *s;
    u_char         *d;
    size_t          len;

    len = size;
    s = src;
    d = dst;

    while (len > 2) {
        *d++ = basis[(s[0] >> 2) & 0x3f];
        *d++ = basis[((s[0] & 3) << 4) | (s[1] >> 4)];
        *d++ = basis[((s[1] & 0x0f) << 2) | (s[2] >> 6)];
        *d++ = basis[s[2] & 0x3f];

        s += 3;
        len -= 3;
    }

    if (len) {
        *d++ = basis[(s[0] >> 2) & 0x3f];

        if (len == 1) {
            *d++ = basis[(s[0] & 3) << 4];
            if (padding) {
                *d++ = padding_char;
            }

        } else {
            *d++ = basis[((s[0] & 3) << 4) | (s[1] >> 4)];
            *d++ = basis[(s[1] & 0x0f) << 2];
        }

        if (padding) {
            *d++ = padding_char;
        }
    }

    return d - dst;
}

